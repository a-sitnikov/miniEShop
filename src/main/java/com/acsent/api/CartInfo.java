package com.acsent.api;

public class CartInfo {

    private Float sum;
    private Long count;

    public CartInfo() {
    }

    public Float getSum() {
        return sum;
    }

    public void setSum(Float sum) {
        this.sum = sum;
    }

    public Long getCount() {
        return count;
    }

    public void setCount(Long count) {
        this.count = count;
    }
}
